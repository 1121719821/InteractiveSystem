package priv.scj.InteractiveSystem.beans;

public class ParentMessage {

	private String parmessageId;
	private String messFrom;
	private String classroom;
	private String content;

	public String getParmessageId() {
		return parmessageId;
	}

	public void setParmessageId(String parmessageId) {
		this.parmessageId = parmessageId;
	}

	public String getMessFrom() {
		return messFrom;
	}

	public void setMessFrom(String messFrom) {
		this.messFrom = messFrom;
	}

	public String getClassroom() {
		return classroom;
	}

	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
