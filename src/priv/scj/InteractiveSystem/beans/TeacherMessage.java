package priv.scj.InteractiveSystem.beans;

public class TeacherMessage {

	private String teamessageId;
	private String messFrom;
	private String classroom;
	private String content;

	public String getTeamessageId() {
		return teamessageId;
	}

	public void setTeamessageId(String teamessageId) {
		this.teamessageId = teamessageId;
	}

	public String getMessFrom() {
		return messFrom;
	}

	public void setMessFrom(String messFrom) {
		this.messFrom = messFrom;
	}

	public String getClassroom() {
		return classroom;
	}

	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
