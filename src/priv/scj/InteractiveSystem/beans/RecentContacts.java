package priv.scj.InteractiveSystem.beans;

public class RecentContacts {

	private String recentContactsDetailsId;
	private String userId;
	private String contacts;

	public String getRecentContactsDetailsId() {
		return recentContactsDetailsId;
	}

	public void setRecentContactsDetailsId(String recentContactsDetailsId) {
		this.recentContactsDetailsId = recentContactsDetailsId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getContacts() {
		return contacts;
	}

	public void setContacts(String contacts) {
		this.contacts = contacts;
	}

}
